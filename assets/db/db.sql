-- Tabla de relación: Rol - Módulo - Permisos
CREATE TABLE SYSTEM_ROL_MODULO_PERMISOS(
    ID INT PRIMARY KEY AUTO_INCREMENT,
    ID_ROL INT NOT NULL,
    ID_MODULO INT NOT NULL,
    ID_PERMISO INT NOT NULL,
    FOREIGN KEY (ID_ROL) REFERENCES SYSTEM_CAT_USUARIOS(ID) ON DELETE CASCADE,
    FOREIGN KEY (ID_MODULO) REFERENCES SYSTEM_MODULOS(ID) ON DELETE CASCADE,
    FOREIGN KEY (ID_PERMISO) REFERENCES SYSTEM_PERMISOS(ID) ON DELETE CASCADE,
    UNIQUE KEY unique_rol_modulo_permiso (ID_ROL, ID_MODULO, ID_PERMISO)
) ENGINE = INNODB;

