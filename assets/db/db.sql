-- Tabla de estudiantes con datos de matrícula
CREATE TABLE VRE_ESTUDIANTES(
    ID INT PRIMARY KEY AUTO_INCREMENT,
    MATRICULA VARCHAR(7) UNIQUE NOT NULL,
    NOMBRE VARCHAR(100) NOT NULL,
    APELLIDO VARCHAR(100) NOT NULL,
    CARRERA VARCHAR(200),
    SEMESTRE INT,
