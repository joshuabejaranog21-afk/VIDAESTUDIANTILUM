SELECT 2, m.ID, p.ID FROM SYSTEM_MODULOS m, SYSTEM_PERMISOS p WHERE p.SLUG IN ('ver','crear','editar');

-- Asignar permisos a EDITOR_INVOLUCRATE (módulos: clubes, ministerios, deportes, instalaciones, cocurriculares)
